export function buildQuestionsUserPrompt(rawQuestion: string): string {
  return [
    '用户原始问题如下。请先判断是否适合本产品的「三问」形态，再输出对应 JSON。',
    '若适合但细节不足，仍应输出完整三问，用稍泛化但可答的选项帮助用户落点；不要轻易走 inScope:false。',
    '',
    `raw_question: ${rawQuestion}`,
  ].join('\n');
}
