export function buildQuestionsUserPrompt(rawQuestion: string): string {
  return [
    '用户原始问题如下，请基于它生成三问。',
    '如果信息不足，使用通用但有效的本质剖析问题，不要追问背景。',
    '',
    `raw_question: ${rawQuestion}`,
  ].join('\n');
}
